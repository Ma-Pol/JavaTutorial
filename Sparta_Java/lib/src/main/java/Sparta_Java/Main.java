package Sparta_Java;

public class Main {
	public static void main(String[] args) {

		// char: 문자
		char a = 'A';
		System.out.println(a);

		// boolean: 논리
		boolean isTrue = true;
		boolean isFalse = false;
		System.out.println(isTrue);
		System.out.println(isFalse);

		// byte: ASCII code
		byte data = '*';
		System.out.println(data);

	}
}